package metier;

/**
 *
 * @author Asus
 */
public class Ingredients {
    private String nom;
    private int id;
    
    public Ingredients(String nom, int id){
        this.id = id;
        this.nom= nom;
    }

    /**
     * @return the nom
     */
    public String getNom() {
        return nom;
    }

    /**
     * @param nom the nom to set
     */
    public void setNom(String nom) {
        this.nom = nom;
    }

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }
}
